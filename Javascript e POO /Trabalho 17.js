// Para manipulação de Strings, podemos usar os métodos .toLowerCase() e .toUpperCase() que retornarão a cadeia de caracteres em letras minúsculas e maisculas, respectivamente

// Exemplo

const minusculas = 'abajur'
const maiusculas = 'CARCAJU'

console.log(minusculas.toUpperCase())
console.log(maiusculas.toLowerCase())
